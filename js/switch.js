let num = +prompt('Виддите цифру от 1 до 4');

switch (num){
    case 1:
        alert('Зима');
        break;
    case 2:
        alert('Весна');
        break;
    case 3:
        alert('Лето');
        break;
    case 4:
        alert('Осень');
        break;
    default:
        alert('Нет таких значений')
}
